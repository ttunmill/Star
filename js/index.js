var main_img = 0;

function fnMain_img(img_num) {
    if(img_num == 0 || img_num) {
        main_img = img_num;
    } else {
        main_img = parseInt(main_img) + 1;
    } if(main_img > $(".main_img").lenght -1) {
        main_img = 0;
    } for (i=0; i<$(".main_img").lenght; i++) {
        $(".main_img").eq(i).fadeOut("slow"); 
    }
    $(".main_img").eq(main_img).fadeIn("slow");
}

setInterval(function() { fnMain_img(); }, 3000);